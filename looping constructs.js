// Practice For loop and While loop

// FOR LOOP

for (var i = 1; i <= 5; i++) {
    console.log(i);
}
console.log('Counting complete');

for (var i = 5; i >= 1; i--) {
    console.log(i);
}
console.log('Counting finished')

// WHILE LOOP

var i = 1 

while (i <= 5) {
    console.log(i);
    i++;
}
console.log('Counting completed!');

var i = 5 

while (i >= 1) {
    console.log(i);
    i = i - 1;
}
console.log('Counting finished!');

var year = 2018

while(year <= 2022) {
    console.log(year);
    year = year + 1;
};